def main():
    string = input("Please enter a string: ")
    change_case(string)

def change_case(x):
    #given a string, returns a string with each upper case letter replaced by a lower case letter and vice-versa

    new_string = ""                       #intialize new string a.k.a. cache for loop

    for y in range(len(x)):
        if x[y].isupper():                        #uppercase to lowercase switch
            new_string += x[y].lower()
        
        else:
            new_string += x[y].upper()            #lowercase to uppercase switch
    
    print(f"Here are the upper and lower cases switched: {new_string}")
            
main()
